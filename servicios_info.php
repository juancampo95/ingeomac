<h1>Principios Básicos Para El Reconocimiento De Una Patología Estructural</h1>
<p>Para iniciar cualquier tipo de investigación patológica a una estructura siempre se deben tener presentes los antecedentes históricos de la misma. Estos antecedentes permitirán a los investigadores tener una idea más clara de cuáles son las causas por las que se pueda estar presentando dicha patología en la estructura. A continuación se hacer referencia a los aspectos que se deben tener claros al inicial un estudio patológico:</p>
<p><span class="negrita">Sismos: </span>Los movimientos telúricos ocurridos durante la vida útil de la estructura.</p>
<p><span class="negrita">Proceso constructivo: </span>Dependiendo de la forma en que estén dispuestos todos los elementos que componen el sistema estructural primario de las edificaciones, se logrará de una forma más acertada diagnosticar las causas y de igual forma dar las recomendaciones para el tipo de intervención que se le deba realizar a la estructura afectada.</p>
<p><span class="negrita">Estado de los materiales: </span>En la calidad y la forma de como sea su composición se podrá determinar por medio de modelación y cálculos matemáticos los parámetros mínimos de resistencia que tiene la estructura. </p>
<p><span class="negrita">Entorno: </span>Depende de la geografía, topografía, características ambientales donde se encuentra ubicada la estructura y de todo su entorno, el que permita que por diferentes causas (físicas, mecánicas o químicas) se puedan estar presentando los síntomas para una patología estructural.</p>
<p><span class="negrita">Identificar adecuadamente las causas de las patologías. </span>Al realizar un estudio patológico para una estructura de concreto reforzado se debe tener muy presente tanto para la recopilación de datos como para la realización de los informes todas las características cualitativas y cuantitativas de cada patología. Estas características para cada caso deben ser entre otras de Forma, Estado, Color, Humedad, Cantidad, Dimensión, Sentido.</p>
<p><span class="negrita">Niveles de riesgo </span>Los niveles de riesgo son indicadores que permiten de primera mano tomar las medidas de seguridad necesarias, para que la integridad de quienes habitan las estructuras afectadas no se vea afectada y de igual forma su funcionamiento dependiendo de sus características. Estos indicadores pueden determinarse por medio de colores, letras o números dependiendo del tipo o lugar donde se lleve a cabo la investigación o estudio patológico. De esta manera podemos identificar tres niveles de riesgo para una estructura de concreto reforzado:</p>
<p><span class="negrita">Nivel de riesgo bajo: </span>Cuando la vulnerabilidad sísmica de la estructura no se ha visto afectada de forma significativa después de un evento o por la afectación directa o indirecta de algún agente externo o interno y que no representa un daño o peligro de colapso para la integridad del sistema estructural. Este nivel de riesgo se puede manifestar en pequeñas fisuras en los elementos de concreto y que muchas veces no se pueden percibir a simple vista.</p>
<p><span class="negrita">Nivel de riesgo medio: </span>Sucede cuando la estructura afectada muestra síntomas o signos puntuales, con fisuras o perdida de recubrimientos que se pueden percibir a simple vista, las cuales pueden comprometer de una manera no muy significativa pero que de igual forma son importantes el sistema estructural de la edificación. En este caso las edificaciones pueden ser utilizadas pero bajo medidas de tratamiento y supervisión.</p>
<p><span class="negrita">Nivel de riesgo alto: </span>Este se da cuando las a las estructuras después de un evento sísmico o después de una grave lesión por causa de alguna patología severa, se le producen desprendimientos parciales o totales de materiales, dejando al descubierto el refuerzo, también se presentan pandeos o deformaciones en los en los elementos. En este caso se recomienda de forma inmediata el desalojo de las estructuras afectadas puesto que su sistema estructural primario esta tan afectado que en cualquier momento se puede presentar un colapso.</p>
