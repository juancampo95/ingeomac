<h1>Suelos</h1>
<p>Los ensayos geotécnicos de laboratorio son pruebas realizadas para la determinación de las características geotécnicas de un terreno. Estos ensayos se ejecutan sobre las muestras previamente obtenidas en el lugar del terreno y, dependiendo del tipo de ensayo, se exigen distintas calidades de muestra.</p>
<p>Para la determinación de las propiedades del suelo, los ensayos se clasifican en:</p>
<p><span class="negrita">Ensayos de identificación:</span> son los únicos (junto con los de compactación) que pueden realizarse sobre muestras alteradas. Pueden ser:</p>
<p>- Físicos: granulometría, plasticidad o peso específico de partículas.</p>
<p>- Químicos: contenido en sulfatos, carbonatos o materia orgánica.</p>
<p><span class="negrita">Ensayos de estado:</span> humedad natural, peso específico seco o aparente. Proporcionan la situación del terreno en su estado natural. Como excepción, pueden utilizarse muestras alteradas para la obtención de la humedad natural, siempre que se protejan de pérdidas posteriores de humedad nada más proceder a su obtención.</p>
<p><span class="negrita">Ensayos de permeabilidad:</span> en permeámetros de carga constante, de carga variable o en célula triaxial.</p>
<p><span class="negrita">Ensayos de cambio de volumen:</span> compresibilidad edométrica, expansividad (presión de hinchamiento, hinchamiento libre, índice de Lambe) y colapso.</p>
<p>Ensayos de resistencia: compresión simple, corte directo (CD, CU, UU), compresión triaxial (CD, CU, UU).</p>
<p><span class="negrita">Otros ensayos sobre suelos o rocas:</span></p>
<p>Compactación Próctor</p>
<p>Índice de dispersividad Pin-Hole (sobre muestra alterada).</p>
<p><span class="negrita">Ensayos sobre rocas:</span> compresión simple (con o sin galgas extensométricas), carga puntual (Point Load), corte directo de diaclasas, índice de durabilidad Slake, compresión triaxial.</p>
<p><span class="negrita">Ensayos químicos sobre agua freática:</span> obtención de pH, de contenido en sales solubles o de elementos contaminantes.</p>
