<h1>Mezcla Aslfaltica</h1>
<p>En una mezcla asfáltica en caliente de pavimentación, el asfalto y el agregado son combinados en proporciones exactas: Las proporciones relativas de estos materiales determinan las propiedades físicas de la mezcla y, eventualmente, el desempeño de la misma como pavimento terminado. Existen dos métodos de diseño comúnmente utilizados para determinar las proporciones apropiadas de asfalto y agregado en una mezcla. Ellos son el método Marshall y el Método Hveem. Ambos métodos de diseño son ampliamente usados en el diseño de mezclas asfálticas de pavimentación. La selección y uso de cualquiera de estos métodos de diseño de mezclas es, principalmente, asuntos de gustos en ingeniería, debido a que cada método contiene características y ventajas singulares. Cualquier método pude ser usado con resultados satisfactorios. </p>
<p>Una muestra de mezcla de pavimentación preparada en el laboratorio puede ser analizada para determinar su posible desempeño en la estructura del pavimento. El análisis está enfocado hacia cuatro características de la mezcla, y la influencia que estas puedan tener en el comportamiento de la mezcla. Las cuatro características son: </p>
<p>1. Densidad de la mezcla</p>
<p>2. Vacíos de aire, o simplemente vacíos.</p>
<p>3. Vacíos en el agregado mineral.</p>
<p>4. Contenido de asfalto.</p>
