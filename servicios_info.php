<h1>Mampostería estructural</h1>
<p>El sistema de mampostería reforzada se fundamenta en la construcción de muros con piezas de mampostería de perforación vertical, unidas por medio de mortero, reforzadas internamente con barras y alambres de acero, cumpliendo los requisitos de análisis, diseño y construcción apropiados establecidos en la NSR - 2010. </p>
