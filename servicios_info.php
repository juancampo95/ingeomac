<h1>Agregados Pétreos</h1>
<p>Es análisis granulométrico es la operación de separar una muestra de agregado en fracciones, cada una de las cuales consta de partículas del mismo tamaño. En la práctica cada fracción contiene partículas que se encuentran dentro de los límites específicos, que son las aberturas de los tamices estándar de muestreo.</p>
<p>El objetivo es determinar el tamaño máximo de los agregados pétreos así como el modulo de finura de la arena, que se refiere a una indicación aproximadamente proporcional del tamaño promedio de la partículas del agregado en prueba, mientras mas bajo es este modulo mas fina es la arena. El análisis granulométrico de un agregado consiste en separar sus diferentes tamaños y determinar los porcentajes de cada tamaño de una muestra de material de grava o arena.</p>
<p>En términos generales, la relación de agregado grueso a fino debe ser mas alta mientras más aumente la finura de la granulometría del agregado fino. Cuando se usa agregado gruesos de roca triturada se necesita una proporción ligeramente mas alta de arena, que cuando se usa un agregado de grava, con el propósito de compensar la menor trabajabilidad causada por la forma afilada y angular de las partículas trituradas de la grava</p>
<p>El tamaño máximo del agregado grueso que se utiliza en el concreto tiene su fundamento, comúnmente se necesita mas agua y cemento para agregados de tamaño pequeño que para tamaños mayores.</p>
